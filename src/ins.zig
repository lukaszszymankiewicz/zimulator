const DataType = u8;
const IndexType = u8;

const MAX_MICROCODE_STEPS = 8;
const MCFuncPtr = *const fn ([]DataType, IndexType) void;
const MCFuncPtrArray = [MAX_MICROCODE_STEPS]MCFuncPtr;
const FSFuncPtr = *const fn ([]i32, DataType) DataType;

pub const instruction_t = struct {
    OP: DataType,
    OPHEX: DataType,
    NMCS: DataType,
    MCS: MCFuncPtrArray,
    ARGS: [MAX_MICROCODE_STEPS]DataType,
    CXFS: FSFuncPtr,
    HXFS: FSFuncPtr,
    NXFS: FSFuncPtr,
    ZXFS: FSFuncPtr,
    comment: []const u8,
    mnemonic: []const u8,

    pub fn init(
        op: DataType,
        ophex: DataType,
        comptime mcs: []const MCFuncPtr,
        comptime args: []const DataType,
        cxfs: FSFuncPtr,
        hxfs: FSFuncPtr,
        nxfs: FSFuncPtr,
        zxfs: FSFuncPtr,
        comment: []const u8,
        mnemonic: []const u8,
    ) instruction_t {
        return instruction_t{
            .OP = op,
            .OPHEX = ophex,
            .NMCS = @intCast(mcs.len),
            .MCS = (mcs ++ [_]MCFuncPtr{undefined} ** (MAX_MICROCODE_STEPS - mcs.len)).*,
            .ARGS = (args ++ [_]DataType{undefined} ** (MAX_MICROCODE_STEPS - args.len)).*,
            .CXFS = cxfs,
            .HXFS = hxfs,
            .NXFS = nxfs,
            .ZXFS = zxfs,
            .comment = comment,
            .mnemonic = mnemonic,
        };
    }
};

